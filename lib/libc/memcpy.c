#include <stddef.h>
#include <string.h>

#pragma coverity compliance \
		(deviate "MISRA C-2012 Rule 21.2" "Reserved keyword or identifier")
void *memcpy(void *dst, const void *src, size_t len)
{
	const char *s = src;
	char *d = dst;

	while (len--)
		*d++ = *s++;

	return dst;
}
